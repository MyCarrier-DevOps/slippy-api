#!/usr/bin/env bash
set -euo pipefail

BASE=${BASE:-http://localhost:8080}
WRITE_API_KEY=${SLIPPY_WRITE_API_KEY:-replacewithenvvarvalue}

CORR="test-$(uuidgen | tr '[:upper:]' '[:lower:]')"
SHA="$(printf '%040d' "$(date +%s)")0000000000000000000000000000000000000000"
SHA="${SHA:0:40}"

step() { echo; echo "в•ђв•ђ> $*"; }
check() {
  local label=$1 code=$2 body=$3
  if [ "$code" = "204" ] || [ "$code" = "201" ] || [ "$code" = "200" ]; then
    echo "    вњ“ HTTP $code вЂ” $label"
    [ -n "$body" ] && echo "$body" | jq . 2>/dev/null || true
  else
    echo "    вњ— HTTP $code вЂ” $label FAILED"
    [ -n "$body" ] && echo "$body"
    exit 1
  fi
}

# run_step <stepName> [label-prefix]
# Starts and completes a pipeline-level step (no component_name body).
run_step() {
  local name=$1 prefix=${2:-}

  step "${prefix}Start $name"
  RAW=$(curl -s -w "\n%{http_code}" \
    -X POST $BASE/v1/slips/$CORR/steps/$name/start \
    -H "Authorization: Bearer $WRITE_API_KEY")
  check "start $name" "$(echo "$RAW" | tail -1)" "$(echo "$RAW" | head -1)"
  sleep 1

  step "${prefix}Complete $name"
  RAW=$(curl -s -w "\n%{http_code}" \
    -X POST $BASE/v1/slips/$CORR/steps/$name/complete \
    -H "Authorization: Bearer $WRITE_API_KEY")
  check "complete $name" "$(echo "$RAW" | tail -1)" "$(echo "$RAW" | head -1)"
}

# в”Ђв”Ђ 1. Create slip в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
step "1. Create slip  corr=$CORR  sha=${SHA:0:12}..."
RAW=$(curl -s -w "\n%{http_code}" -X POST $BASE/v1/slips \
  -H "Authorization: Bearer $WRITE_API_KEY" \
  -H "Content-Type: application/json" \
  -d "{
    \"correlation_id\": \"$CORR\",
    \"repository\":     \"MyCarrier-DevOps/my-service\",
    \"branch\":         \"main\",
    \"commit_sha\":     \"$SHA\",
    \"components\": [
      {\"name\": \"api\",    \"dockerfile_path\": \"services/api/Dockerfile\"},
      {\"name\": \"worker\", \"dockerfile_path\": \"services/worker/Dockerfile\"}
    ]
  }")
BODY=$(echo "$RAW" | head -1)
CODE=$(echo "$RAW" | tail -1)
check "create slip" "$CODE" "$BODY"
echo "    correlation_id : $(echo "$BODY" | jq -r '.slip.correlation_id')"
echo "    status         : $(echo "$BODY" | jq -r '.slip.status')"
echo "    ancestry       : $(echo "$BODY" | jq -r '.ancestry_resolved')"
# builds is auto-started by CreateSlipForPush at the pipeline level

# в”Ђв”Ђ 2. unit_tests в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step unit_tests "2. "

# в”Ђв”Ђ 3. secret_scan в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step secret_scan "3. "

# в”Ђв”Ђ 4. package_artifact в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step package_artifact "4. "

# в”Ђв”Ђ 5. builds (aggregate: api + worker) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
for COMPONENT in api worker; do
  step "5. Start builds вЂ“ $COMPONENT"
  RAW=$(curl -s -w "\n%{http_code}" \
    -X POST $BASE/v1/slips/$CORR/steps/builds/start \
    -H "Authorization: Bearer $WRITE_API_KEY" \
    -H "Content-Type: application/json" \
    -d "{\"component_name\": \"$COMPONENT\"}")
  check "start builds/$COMPONENT" "$(echo "$RAW" | tail -1)" "$(echo "$RAW" | head -1)"
  sleep 1

  step "5. Complete builds вЂ“ $COMPONENT"
  RAW=$(curl -s -w "\n%{http_code}" \
    -X POST $BASE/v1/slips/$CORR/steps/builds/complete \
    -H "Authorization: Bearer $WRITE_API_KEY" \
    -H "Content-Type: application/json" \
    -d "{\"component_name\": \"$COMPONENT\"}")
  check "complete builds/$COMPONENT" "$(echo "$RAW" | tail -1)" "$(echo "$RAW" | head -1)"
done

# в”Ђв”Ђ 6. dev_deploy в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step dev_deploy "6. "

# в”Ђв”Ђ 7. dev_tests в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step dev_tests "7. "

# в”Ђв”Ђ 8. preprod_deploy в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step preprod_deploy "8. "

# в”Ђв”Ђ 9. preprod_tests в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step preprod_tests "9. "

# в”Ђв”Ђ 10. prod_gate в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step prod_gate "10. "

# в”Ђв”Ђ 11. prod_release_created в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step prod_release_created "11. "

# в”Ђв”Ђ 12. prod_deploy в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step prod_deploy "12. "

# в”Ђв”Ђ 13. prod_tests в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run_step prod_tests "13. "


# в”Ђв”Ђ Final read-back в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
step "Final state вЂ” GET /slips/$CORR"
RAW=$(curl -s -w "\n%{http_code}" $BASE/slips/$CORR \
  -H "Authorization: Bearer $WRITE_API_KEY")
BODY=$(echo "$RAW" | head -1)
CODE=$(echo "$RAW" | tail -1)
check "read slip" "$CODE" ""
echo
echo "  Overall status : $(echo "$BODY" | jq -r '.status')"
echo "  Step statuses  :"
echo "$BODY" | jq -r '.steps | to_entries[] | "    \(.key): \(.value.status)"'
